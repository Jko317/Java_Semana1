    private void tablaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tablaKeyReleased
        // TODO add your handling code here:
        int fila=tabla.getSelectedRow();
        String k=(String)tabla.getValueAt(fila,0);
        for(int x=0;x<=5;x++){
            if(k.equals(P[0][x])){
                tabla.setValueAt(P[0][x],fila,0);
                tabla.setValueAt(P[1][x],fila,1);
                tabla.setValueAt(P[2][x],fila,2);
                
                int a,b,c,ft=0;
                          
                a=Integer.parseInt(P[2][x]);
                b=Integer.parseInt((String)tabla.getValueAt(fila,3));
                c=a*b;
                
                valor=c+valor;
                tabla.setValueAt(c,fila,4);
                total.setText(Double.toString(valor));
                nom=(cliente.getText());
                resultado.setText("Señor "+nom+", el total de su compra es: $"+valor );
                
            }
      
        }
    }//GEN-LAST:event_tablaKeyReleased

